"""
Operadores in e not in
Strings são iteráveis, ou seja, podemos acessar seus elementos através do índice.
"""

nome = "Ricardo"
print(nome[2]) # Output: c
print(nome[-1]) # Output: o

print('a' in nome) # Output: True
print('z' in nome) # Output: False


print("cardo" not in nome) # Output: False