# Tipos int e float

# int -> Números inteiros
# O tipo int é usado para representar números inteiros, ou seja, números sem parte decimal.
print(11) # int
print(-11) #int
print(0) # int

# float -> Números de ponto flutuante
# O tipo float é usado para representar números com parte decimal, ou seja, números reais.
print(1.1, 10.11) # float
print(0.0, -1.5) # float

# A função type é usada para verificar o tipo de uma variável.
print(type('Otávio'))
print(type(0))
print(type(1.1), type(-1.1), type(0.0))

